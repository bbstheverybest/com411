print("Towards which direction should I go (up, down, left or right)?")
direction=input()
if direction=="up":
    print("I am now moving in an upward direction")
elif direction=="down":
    print("I am now moving in an downward direction")
elif direction=="left":
    print("I am now moving in an left direction")
elif direction=="right":
    print("I am now moving in an right direction")







